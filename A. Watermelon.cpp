#include<bits/stdc++.h>
#define ll long long
using namespace std;
int main()
{
   ll n;
   cin>>n;
   if(n<0) cout<<"NO"<<endl;
   else if(n==0) cout<<"NO"<<endl;
   else if(n==2) cout<<"NO"<<endl;
   else if(n%2==0)
   {
       cout<<"YES"<<endl;
   }
   else{
       cout<<"NO"<<endl;
   }



    return 0;
}
